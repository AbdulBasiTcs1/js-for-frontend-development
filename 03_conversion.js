// Datatype conversion

// JavaScript converts types automatically.
let result1 = "5" + 2;
let result2 = "5" - 2;

console.log(result1); // Output: 52
console.log(result2); // Output: 3

// Developer converts the data type.

// String -> Number
let age = "21";

console.log(Number(age));  // Output: 21
console.log(parseInt(age)); // Output: 21

// Number->String
let score = 100;

console.log(String(score)); // Output: "100"

// Boolean Conversion 
console.log(Boolean(1));   // Output: true
console.log(Boolean(0));   // Output: false
console.log(Boolean(""));  // Output: false
console.log(Boolean("ABDUL BASIT"))

