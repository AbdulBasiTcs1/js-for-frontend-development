// Primitive Data Types

let name = "Basit";        // String
let age = 21;             // Number
let isStudent = true;     // Boolean
let score = null;         // Null or Object
let city;                 // Undefined

console.log(name);        // Output: Basit
console.log(age);         // Output: 21
console.log(isStudent);   // Output: true
console.log(score);       // Output: null
console.log(city);        // Output: undefined

// typeof operator
console.log(typeof name); // Output: string
console.log(typeof age);  // Output: number
console.log(typeof score); // Output: object (JS bug)

// Non-Primitive (Reference) Data Types

let skills = ["HTML", "CSS", "JS"];   // Array
let user = { name: "Basit", age: 21 }; // Object

console.log(skills); // Output: ["HTML", "CSS", "JS"]
console.log(user);   // Output: { name: "Basit", age: 21 }

